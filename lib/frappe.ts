// ─── 6. Paystack Transfer (salary disbursement) ──────────────────────────────

/**
 * Disburse net salary to a candidate via Paystack Transfer API.
 * Called after a salary slip is submitted and confirmed.
 *
 * Paystack supports: bank transfers, M-Pesa (KE), MTN MoMo (GH, UG, RW), Airtel Money
 */
export async function disburseSalary(params: {
  recipientCode:  string;   // Paystack recipient code (stored on candidate profile)
  amountKobo:     number;   // amount in smallest currency unit (kobo for NGN, cents for KES)
  reference:      string;   // unique reference e.g. `payslip-${slipId}`
  reason:         string;   // e.g. "March 2026 salary — Jorbex"
}): Promise<string> {
  const PAYSTACK_SECRET = process.env.PAYSTACK_SECRET_KEY || '';
  if (!PAYSTACK_SECRET) throw new Error('[Frappe/Paystack] PAYSTACK_SECRET_KEY not set');

  try {
    const res = await axios.post(
      'https://api.paystack.co/transfer',
      {
        source:    'balance',
        amount:    params.amountKobo,
        recipient: params.recipientCode,
        reason:    params.reason,
        reference: params.reference,
      },
      {
        headers: {
          Authorization:  `Bearer ${PAYSTACK_SECRET}`,
          'Content-Type': 'application/json',
        },
      }
    );
    return res.data?.data?.transfer_code;
  } catch (err: unknown) {
    if (isAxiosError(err)) {
      console.error('[Frappe/Paystack] disburseSalary failed:', err.response?.data || err.message);
      throw new Error(err.response?.data?.message || 'Salary disbursement failed');
    }
    throw err;
  }
}

// ─── Helpers ─────────────────────────────────────────────────────────────────

function getMonthStart(year: number, month: number): string {
  return `${year}-${String(month).padStart(2, '0')}-01`;
}

function getMonthEnd(year: number, month: number): string {
  const lastDay = new Date(year, month, 0).getDate();
  return `${year}-${String(month).padStart(2, '0')}-${lastDay}`;
}
