/**
 * Fetch all salary slips for an employee.
 * Optionally filter by year/month.
 */
export async function getEmployeeSalarySlips(
  frappeEmployeeName: string,
  year?: number,
  month?: number
): Promise<SalarySlip[]> {
  const client = getClient();

  const filters: [string, string, string][] = [
    ['employee', '=', frappeEmployeeName],
    ['docstatus', '=', '1'],  // submitted only
  ];

  if (year && month) {
    filters.push(['start_date', '>=', getMonthStart(year, month)]);
    filters.push(['end_date',   '<=', getMonthEnd(year, month)]);
  }

  try {
    const res = await client.get('/api/resource/Salary Slip', {
      params: {
        filters: JSON.stringify(filters),
        fields:  JSON.stringify([
          'name', 'employee', 'employee_name',
          'start_date', 'end_date', 'posting_date',
          'gross_pay', 'total_deduction', 'net_pay', 'currency',
        ]),
        order_by: 'start_date desc',
        limit: 24,
      },
    });
    return res.data?.data ?? [];
  } catch (err: unknown) {
    if (isAxiosError(err)) {
      throw new Error(err.response?.data?.exc_type || 'Failed to fetch salary slips');
    }
    throw err;
  }
}

/**
 * Download a salary slip as a PDF binary buffer.
 * Store this in S3 and return the URL for the candidate portal.
 */
export async function getSalarySlipPDF(slipName: string): Promise<Buffer> {
  const client = getClient();
  try {
    const res = await client.get('/api/method/frappe.utils.print_format.download_pdf', {
      params: {
        doctype:       'Salary Slip',
        name:          slipName,
        format:        'Standard',
        no_letterhead: 0,
      },
      responseType: 'arraybuffer',
    });
    return Buffer.from(res.data);
  } catch {
    throw new Error('Failed to download salary slip PDF');
  }
}

/**
 * Map a raw Frappe SalarySlip to a clean PayslipSummary for the Jorbex portal.
 */
export function mapToPayslipSummary(slip: SalarySlip, pdfUrl?: string): PayslipSummary {
  const date = new Date(slip.start_date);
  const month = date.toLocaleString('en-US', { month: 'long', year: 'numeric' });
  return {
    slipId:       slip.name,
    employeeId:   slip.employee,
    employeeName: slip.employee_name,
    month,
    grossPay:     slip.gross_pay,
    netPay:       slip.net_pay,
    deductions:   slip.total_deduction,
    currency:     slip.currency,
    pdfUrl,
    components:   [...(slip.earnings ?? []), ...(slip.deductions ?? [])],
  };
}

// ─── 4. Full Onboarding Flow (single call from API route) ───────────────────

/**
 * Onboard a new employee end-to-end:
 *   1. Create employee in Frappe HR
 *   2. Assign salary structure for their country
 *
 * Call this from /api/v1/eor/onboard when an offer is accepted cross-border.
 * Returns the Frappe employee name (e.g. "HR-EMP-00001") to store in EmploymentRecord.
 */
export async function onboardEmployee(params: {
  jorbexCandidateId: string;
  firstName:         string;
  lastName:          string;
  email:             string;
  phone?:            string;
  dateOfBirth:       string;   // YYYY-MM-DD
  gender:            'Male' | 'Female' | 'Other';
  startDate:         string;   // YYYY-MM-DD
  country:           SupportedCountry;
  grossMonthlySalary: number;
  currency:          string;
  designation?:      string;
  // Nigeria-specific
  taxId?:            string;
  pencomNumber?:     string;
  // Kenya-specific
  kraPin?:           string;
  nhifNumber?:       string;
  nssfNumber?:       string;
}): Promise<string> {
  // 1 — Create employee
  const frappeEmployeeName = await createEmployee({
    employee_number: params.jorbexCandidateId,
    first_name:      params.firstName,
    last_name:       params.lastName,
    personal_email:  params.email,
    cell_number:     params.phone,
    date_of_birth:   params.dateOfBirth,
    gender:          params.gender,
    date_of_joining: params.startDate,
    company:         FRAPPE_COMPANY,
    country:         params.country,
    designation:     params.designation,
    // Nigeria
    tax_id:          params.taxId,
    pf_number:       params.pencomNumber,
    // Kenya
    kra_pin:         params.kraPin,
    nhif_number:     params.nhifNumber,
    nssf_number:     params.nssfNumber,
  });

  // 2 — Assign salary structure
  await assignSalaryStructure({
    employee:         frappeEmployeeName,
    salary_structure: getSalaryStructureName(params.country),
    from_date:        params.startDate,
    base:             params.grossMonthlySalary,
    currency:         params.currency,
  });

  return frappeEmployeeName;
}

