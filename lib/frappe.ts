/**
 * lib/frappe.ts
 *
 * Frappe HR / ERPNext REST API client for Jorbex payroll integration.
 *
 * Covers the full employee lifecycle:
 *   1. Create employee record
 *   2. Assign salary structure
 *   3. Generate salary slip (payslip)
 *   4. Fetch payslip data + PDF
 *   5. Disburse salary via Paystack Transfer
 *
 * Country support: Nigeria (PAYE, PENCOM, NHF, NSITF) + Kenya (PAYE, SHIF, NSSF, Housing Levy)
 *
 * Authentication: Token-based — Authorization: token api_key:api_secret
 * Base URL: https://{FRAPPE_HOST}/api/resource/{DocType}
 */

import axios, { AxiosInstance, isAxiosError } from 'axios';

// ─── Config ──────────────────────────────────────────────────────────────────

const FRAPPE_HOST    = process.env.FRAPPE_HOST    || '';   // e.g. https://payroll.jorbex.com
const FRAPPE_API_KEY = process.env.FRAPPE_API_KEY || '';
const FRAPPE_SECRET  = process.env.FRAPPE_API_SECRET || '';
const FRAPPE_COMPANY = process.env.FRAPPE_COMPANY || 'Jorbex';

// ─── Types ───────────────────────────────────────────────────────────────────

export type SupportedCountry = 'NG' | 'KE' | 'GH' | 'ZA' | 'UG' | 'TZ' | 'RW';

export interface FrappeEmployee {
  name?:            string;   // auto-generated by Frappe e.g. "HR-EMP-00001"
  employee_number?: string;   // your internal ID (Jorbex candidate ID)
  first_name:       string;
  last_name?:       string;
  date_of_birth:    string;   // YYYY-MM-DD
  gender:           'Male' | 'Female' | 'Other';
  date_of_joining:  string;   // YYYY-MM-DD
  company:          string;
  department?:      string;
  designation?:     string;
  employment_type?: string;
  personal_email?:  string;
  cell_number?:     string;
  country:          string;   // ISO2 e.g. NG, KE
  // Nigeria-specific
  pf_number?:       string;   // PENCOM pension fund account number
  tax_id?:          string;   // Nigeria TIN
  // Kenya-specific
  nhif_number?:     string;
  nssf_number?:     string;
  kra_pin?:         string;
}

export interface SalaryStructureAssignment {
  employee:          string;   // Frappe employee name
  salary_structure:  string;   // e.g. "Nigeria-Standard" or "Kenya-Standard"
  from_date:         string;   // YYYY-MM-DD
  base:              number;   // monthly gross salary (in local currency)
  currency:          string;   // ISO currency code e.g. NGN, KES
}

export interface SalarySlip {
  name:            string;   // Frappe slip ID e.g. "Salary Slip/00001"
  employee:        string;
  employee_name:   string;
  start_date:      string;
  end_date:        string;
  posting_date:    string;
  gross_pay:       number;
  total_deduction: number;
  net_pay:         number;
  currency:        string;
  earnings:        SalaryComponent[];
  deductions:      SalaryComponent[];
  docstatus:       0 | 1 | 2;  // 0=Draft, 1=Submitted, 2=Cancelled
}

export interface SalaryComponent {
  salary_component: string;   // e.g. "PAYE Tax", "PENCOM Pension"
  amount:           number;
}

export interface PayslipSummary {
  slipId:       string;
  employeeId:   string;
  employeeName: string;
  month:        string;   // "March 2026"
  grossPay:     number;
  netPay:       number;
  deductions:   number;
  currency:     string;
  pdfUrl?:      string;   // S3 URL after we store it
  components:   SalaryComponent[];
}

// ─── HTTP Client ─────────────────────────────────────────────────────────────

function getClient(): AxiosInstance {
  if (!FRAPPE_HOST || !FRAPPE_API_KEY || !FRAPPE_SECRET) {
    throw new Error('[Frappe] FRAPPE_HOST, FRAPPE_API_KEY, and FRAPPE_API_SECRET must be set');
  }
  return axios.create({
    baseURL: FRAPPE_HOST,
    headers: {
      Authorization: `token ${FRAPPE_API_KEY}:${FRAPPE_SECRET}`,
      'Content-Type': 'application/json',
    },
  });
}

// ─── 1. Employee Management ──────────────────────────────────────────────────

/**
 * Create a new employee record in Frappe HR.
 * Called when an offer is accepted and EOR onboarding is triggered.
 */
export async function createEmployee(data: FrappeEmployee): Promise<string> {
  const client = getClient();
  try {
    const res = await client.post('/api/resource/Employee', {
      ...data,
      company: data.company || FRAPPE_COMPANY,
      status: 'Active',
    });
    const employeeName: string = res.data?.data?.name;
    if (!employeeName) throw new Error('Employee created but no name returned');
    return employeeName;
  } catch (err: unknown) {
    if (isAxiosError(err)) {
      console.error('[Frappe] createEmployee failed:', err.response?.data || err.message);
      throw new Error(err.response?.data?.exc_type || 'Failed to create employee in Frappe HR');
    }
    throw err;
  }
}

/**
 * Fetch an employee record by their Frappe employee name or employee_number.
 */
export async function getEmployee(frappeEmployeeName: string): Promise<FrappeEmployee> {
  const client = getClient();
  try {
    const res = await client.get(`/api/resource/Employee/${frappeEmployeeName}`);
    return res.data?.data;
  } catch (err: unknown) {
    if (isAxiosError(err)) {
      throw new Error(err.response?.data?.exc_type || 'Failed to fetch employee');
    }
    throw err;
  }
}

/**
 * Update an employee record (e.g. on profile change).
 */
export async function updateEmployee(
  frappeEmployeeName: string,
  updates: Partial<FrappeEmployee>
): Promise<void> {
  const client = getClient();
  try {
    await client.put(`/api/resource/Employee/${frappeEmployeeName}`, updates);
  } catch (err: unknown) {
    if (isAxiosError(err)) {
      throw new Error(err.response?.data?.exc_type || 'Failed to update employee');
    }
    throw err;
  }
}

// ─── 2. Salary Structure Assignment ─────────────────────────────────────────

/**
 * Assign a salary structure to an employee.
 * The salary structure must already exist in Frappe (created once during setup).
 * For Nigeria use "Nigeria-Standard", for Kenya use "Kenya-Standard".
 */
export async function assignSalaryStructure(data: SalaryStructureAssignment): Promise<void> {
  const client = getClient();
  try {
    await client.post('/api/resource/Salary Structure Assignment', {
      employee:         data.employee,
      salary_structure: data.salary_structure,
      from_date:        data.from_date,
      base:             data.base,
      currency:         data.currency,
      docstatus:        1,   // submit immediately
    });
  } catch (err: unknown) {
    if (isAxiosError(err)) {
      console.error('[Frappe] assignSalaryStructure failed:', err.response?.data || err.message);
      throw new Error(err.response?.data?.exc_type || 'Failed to assign salary structure');
    }
    throw err;
  }
}

/**
 * Returns the correct salary structure name for a given country.
 * These structures must be configured once in your Frappe instance.
 */
export function getSalaryStructureName(country: SupportedCountry): string {
  const map: Record<SupportedCountry, string> = {
    NG: 'Nigeria-Standard',
    KE: 'Kenya-Standard',
    GH: 'Ghana-Standard',
    ZA: 'SouthAfrica-Standard',
    UG: 'Uganda-Standard',
    TZ: 'Tanzania-Standard',
    RW: 'Rwanda-Standard',
  };
  return map[country] ?? 'Africa-Basic';
}

// ─── 3. Salary Slip (Payslip) ────────────────────────────────────────────────

/**
 * Generate a salary slip for a single employee for a given month.
 * Creates in draft (docstatus=0), then submits (docstatus=1).
 * Returns the Frappe slip name e.g. "Salary Slip/00001".
 */
export async function generateSalarySlip(
  frappeEmployeeName: string,
  year: number,
  month: number   // 1-12
): Promise<string> {
  const client = getClient();
  const startDate = getMonthStart(year, month);
  const endDate   = getMonthEnd(year, month);

  try {
    // Step 1 — Create draft salary slip
    const createRes = await client.post('/api/resource/Salary Slip', {
      employee:   frappeEmployeeName,
      start_date: startDate,
      end_date:   endDate,
      docstatus:  0,
    });

    const slipName: string = createRes.data?.data?.name;
    if (!slipName) throw new Error('Salary slip created but no name returned');

    // Step 2 — Submit the slip (docstatus=1 triggers tax + deduction calculations)
    await client.put(`/api/resource/Salary Slip/${slipName}`, { docstatus: 1 });

    return slipName;
  } catch (err: unknown) {
    if (isAxiosError(err)) {
      const errorBody = err.response?.data || err.message;
      console.error('[Frappe] generateSalarySlip failed:', errorBody);
      throw new Error(err.response?.data?.exc_type || 'Failed to generate salary slip');
    }
    throw err;
  }
}

/**
 * Generate a salary slip with custom Jorbex Office earnings (Hourly + OT).
 */
export async function generateSalarySlipWithEarnings(params: {
  frappeEmployeeName: string;
  year: number;
  month: number;
  earnings: SalaryComponent[];
}): Promise<string> {
  const client = getClient();
  const startDate = getMonthStart(params.year, params.month);
  const endDate   = getMonthEnd(params.year, params.month);

  try {
    // 1 — Create draft salary slip with injected earnings
    const createRes = await client.post('/api/resource/Salary Slip', {
      employee:   params.frappeEmployeeName,
      start_date: startDate,
      end_date:   endDate,
      docstatus:  0,
      earnings:   params.earnings, // Direct injection into the earnings table
    });

    const slipName = createRes.data?.data?.name;
    if (!slipName) throw new Error('Salary slip creation failed');

    // 2 — Submit the slip
    await client.put(`/api/resource/Salary Slip/${slipName}`, { docstatus: 1 });

    return slipName;
  } catch (err: unknown) {
    if (isAxiosError(err)) {
      const errorBody = err.response?.data || err.message;
      console.error('[Frappe] generateSalarySlipWithEarnings failed:', errorBody);
      throw new Error(err.response?.data?.exc_type || 'Failed to generate hourly salary slip');
    }
    throw err;
  }
}

